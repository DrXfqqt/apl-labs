public class Main {
    public static void main(String[] args) {
        System.out.println("Value of 10&50 is: " + (10&50));
        System.out.println("Value of 30|40 is: " + (30|40));
        System.out.println("Value of ~50 is: " + (~50));
        System.out.println("Value of 60>>3 is: " + (60>>3));
        System.out.println("Value of 70<<8 is: " + (70<<8));
        System.out.println("Value of 55>>>2 is: " + (55>>>2));
    }
}
